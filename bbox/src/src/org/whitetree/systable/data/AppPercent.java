package org.whitetree.systable.data;

public class AppPercent {
	String mPkgName;
	String mPercent;

	public String getPkgName() {
		return mPkgName;
	}

	public void setPkgName(String pkgName) {
		mPkgName = pkgName;
	}

	public String getPercent() {
		return mPercent;
	}

	public void setPercent(String percent) {
		mPercent = percent;
	}
}
